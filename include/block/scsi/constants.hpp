//! Sense keys
enum class sense_key : uint8_t
{
  no_sense            = 0x00,
  recovered_error     = 0x01,
  not_ready           = 0x02,
  medium_error        = 0x03,
  hardware_error      = 0x04,
  illegal_request     = 0x05,
  unit_attention      = 0x06,
  data_protection     = 0x07,
  blank_check         = 0x08,
  vendor_specific     = 0x09,
  copy_aborted        = 0x0a,
  command_aborted     = 0x0b,
  obsolete_0c         = 0x0c,
  volume_overflow     = 0x0d,
  miscompare          = 0x0e,
  reserved_0f         = 0x0f
};
std::string to_str(const sense_key& _e);

//! Inquiry code pages
enum class code_page : uint8_t
{
  supported_vpd_pages                 = 0x00,  //! mandatory
  unit_serial_number                  = 0x80,
  device_identification               = 0x83,  //! mandatory
  software_interface_identification   = 0x84,
  management_network_address          = 0x85,
  extended_inquiry_data               = 0x86,
  mode_page_policy                    = 0x87,
  scsi_ports                          = 0x88,
  block_limits                        = 0xb0,
  block_device_characteristics        = 0xb1,
  logical_block_provisioning          = 0xb2,
  // 0x89 to 0xaf = reserved
  // 0xb0 - 0xbf = device specific
  // 0xc0 to 0xff = vendor-specific
  custom_vpd                          = 0xff  //! custom code page. may be vendor-specific
};
std::string to_str(const code_page& _e);

//! Peripheral qualifier is a 3-bit field
enum class peripheral_qualifier : int8_t
{
  connected          = 0x00,
  disconnected       = 0x01,
  reserved_02        = 0x02,
  not_supported      = 0x03,
  vendor_specific_04 = 0x04, //! vendor-specific
  vendor_specific_05 = 0x05,
  vendor_specific_06 = 0x06,
  vendor_specific_07 = 0x07
};
std::string to_str(const peripheral_qualifier& _e);

