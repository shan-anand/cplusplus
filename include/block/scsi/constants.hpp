enum class protocol_id : uint8_t
{
  fiber_channel = 0x00,
  parallel_scsi = 0x01,
  ssa           = 0x02,
  ieee_1394     = 0x03,
  rdma          = 0x04,
  iscsi         = 0x05,
  sas           = 0x06,
  adt           = 0x07,
  ata           = 0x08,
  reserved_9    = 0x09
};
